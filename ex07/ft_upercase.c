char	*ft_strupcase(char *str)
{
	int i;
	char c;

	i = 0;
	c = *str;
	while (c != 0)
	{
        	if(c >= 97 && c <= 122)
		{
			str[i] = c - 32;
		}
		i++;
		c = str[i];
	}
	return (str);
}
